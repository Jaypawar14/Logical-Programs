package LogicalPrograms;

public class SwappingTwonumber {

	public static void main(String[] args) {

		int a=10,b=20;  //Exp - a=20,b=10
		
		System.out.println("Before Swappong values :"+a+" "+b);
		
		int C = a;  //10
		a=b;  //20
		b=C;  //10
		
		System.out.println("Before Swappong values :"+a+" "+b);

	}

}
